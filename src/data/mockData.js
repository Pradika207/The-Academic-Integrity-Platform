export const calculateRiskScore = (student) => {
  const weights = {
    attendance: 0.25,
    cgpa: 0.25,
    internalMarks: 0.2,
    projects: 0.15,
    behavior: 0.1,
    assessment: 0.05
  };

  const attendanceRisk = (100 - student.attendance);
  const cgpaRisk = (10 - student.cgpa) * 10;
  
  const avgInternal = student.internalMarks?.length > 0 
    ? student.internalMarks.reduce((acc, m) => acc + m.marks, 0) / student.internalMarks.length 
    : 50;
  const internalRisk = (100 - avgInternal);
  
  const completedProjects = student.projects?.filter(p => p.status === 'Completed').length || 0;
  const projectRisk = Math.max(0, 100 - (completedProjects * 50)); // Assuming 2 projects is ideal

  const assessmentRisk = (100 - student.assessmentAvg);
  const behaviorRisk = (100 - student.behaviorScore);

  const totalRisk = (
    attendanceRisk * weights.attendance +
    cgpaRisk * weights.cgpa +
    internalRisk * weights.internalMarks +
    projectRisk * weights.projects +
    behaviorRisk * weights.behavior +
    assessmentRisk * weights.assessment
  );

  return Math.round(totalRisk);
};

export const getRiskLevel = (score) => {
  if (score > 70) return 'High Risk';
  if (score > 40) return 'Medium Risk';
  return 'Low Risk';
};

export const getStatusColor = (score) => {
  if (score > 70) return '#f43f5e'; // Danger
  if (score > 40) return '#f59e0b'; // Warning
  return '#10b981'; // Success
};

